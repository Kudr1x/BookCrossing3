package max51.com.vk.bookcrossing.ui.f2;

public class Elements {
    public String s1;
    public String s2;
    public int image;

    public String getS1() {
        return s1;
    }

    public void setS1(String s1) {
        this.s1 = s1;
    }

    public String getS2() {
        return s2;
    }

    public void setS2(String s2) {
        this.s2 = s2;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public Elements(String s1, String s2, int image) {
        this.s1 = s1;
        this.s2 = s2;
        this.image = image;
    }
}
